export function getResponse(name, msg) {
  const m = msg.toLowerCase();

  // 📦 پلن‌ها
  if (m === "/plans" || m.includes("پلن")) {
    return `*سلام ${name}!* 📦 لیست پلن‌ها:\n\n🥉 برنز – ۲۵۷.۰۰۰ /۱۰۰۰ عضو | عمومی | رشد سریع\n🥈 نقره – ۳۴۷.۵۰۰ /۱۰۰۰ عضو | فیلم/سریال | کاربران فعال\n🥇 طلا – ۳۶۲.۵۰۰ /۱۰۰۰ عضو | تعامل بالا\n💎 الماس – ۵۰k+ عضو | ترکیب همه | استعلام قیمت\n\n📩 سفارش: @Mohammadzadehads`;
  }

  // 💳 پرداخت
  if (m.includes("پرداخت")) {
    return `💳 *${name} جان،* کارت به کارت یا ارز دیجیتال می‌تونی پرداخت کنی 💰 جزئیات: @Mohammadzadehads`;
  }

  // 💰 قیمت
  if (m.includes("قیمت")) {
    return `💰 *سلام ${name}!* لیست پلن‌ها /plans 👇 ارزان‌ترینش برنز ۲۵۷k هست`;
  }

  // 📞 پشتیبانی
  if (m.includes("پشتیبانی") || m.includes("تماس")) {
    return `📞 *${name} جان،* پشتیبانی ۲۴/۷ در @Mohammadzadehads`;
  }

  // شروع گفتگو مرحله‌ای
  if (m === "شروع") {
    return `*سلام ${name}!* موضوع کانالت چیه؟ (فیلم/فروش/...)`;
  }

  // جواب کلی
  return `*${name} جان،* سؤالت "${msg}" بود.\n📈 به نظرم پلن نقره برات عالیه چون تعامل بالایی داره.\n💰 قیمت: ۳۴۷.۵۰۰ تومان 👇 @Mohammadzadehads`;
}

